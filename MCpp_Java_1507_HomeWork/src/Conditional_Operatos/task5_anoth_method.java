package Conditional_Operatos;

//Написать программу определения оценки студента по его рейтингу, на основе следующих правил:
//0-19=F 20-39=E 40-59=D 60-74=C 75-89=B 90-100=A
public class task5_anoth_method {

	public static void main(String[] args) {
		int a=77;
		char res=0;
		
		if (a>=0 && a<=19)
		{
			res = 'F';
		}
		
		else if (a>=20 && a<=39)
		{
			res = 'E';
		}
		
		else if (a>=40 && a<=59)
		{
			res = 'D';
		}
		
		else if (a>=60 && a<=74)
		{
			res = 'С';
		}
		
		else if (a>=75 && a<=89) 
		{
			res = 'B';
		}
		
		else if (a>=90 && a<=100)
			res = 'A';
		
		
		
		System.out.println(res);
	}

}
