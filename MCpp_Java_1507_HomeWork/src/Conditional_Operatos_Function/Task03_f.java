package Conditional_Operatos_Function;
//Найти суммы только положительных из трех чисел
public class Task03_f {

	public static void main(String[] args) 
	{
		int a=-1;
		int b=-2;
		int c=5;
		
		int result = count(a, b, c);
		System.out.println(result);
		
	}
	
	public static int count (int a, int b, int c)
	{
		int resultCount=0;
		if (a>0) 
		{
			resultCount = resultCount +a; 
		}
		if (b>0) 
		{
			resultCount = resultCount + b;
		}
		if (c>0) 
		{
			resultCount = resultCount + c;
		}
		return resultCount;
	}

}
