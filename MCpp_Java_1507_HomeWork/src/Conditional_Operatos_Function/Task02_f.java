public class Task02_f 
{

	public static void main(String[] args) 
	{
		int x=0;
		int y=0;
		
		int result = count(x, y);
		
		System.out.println(result);
	}
	
	public static int count (int x, int y )
	{
		int resultCount=0;
		
		if (x>0 && y>0) 
		{
			resultCount =1;	
		}

		else if (x<0 && y>0)
		{
			resultCount = 2;
		}

		else if (x<0 && y<0) 
		{
			resultCount = 3;		
		}

		else resultCount = 4;

	
		return resultCount;
	}

}
