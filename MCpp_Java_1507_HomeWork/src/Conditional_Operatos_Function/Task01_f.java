package Conditional_Operatos_Function;
//Если а – четное посчитать а*б, иначе а+б

public class Task01_f 
{
	public static void main(String[] args) 
	{
		int a=-2;
		int b=20;
		int res = count(a, b);

		System.out.println(res);
	} 

	public static int count(int a, int b)
	{
		int countResult=0;
		if (a%2==0) 
		{
			countResult=a*b;
		}

		else 
		{
			countResult = a+b;
		}
		return countResult;
	}

}
