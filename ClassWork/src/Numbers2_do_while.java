
public class Numbers2_do_while {

	public static void main(String[] args) {
		// ВЫВОД НА КОНСОЛЬ ЧИСЕЛ ОТ 20 ДО 50
		int i = 20;

		do {
			System.out.print(i+ " ");
			i++;
		} while (i<=50);
		System.out.println();
		System.out.println();


		// ВЫВОД НА КОНСОЛЬ ЧИСЕЛ ОТ -10 ДО 20
		int c=-10;
		do {
			System.out.print(c + " ");
			c++;
		} while (c<=20);
		System.out.println();
		System.out.println();
		// ВЫВОД НА КОНСОЛЬ ЧИСЕЛ ОТ 30 ДО 0
		int d = 30;

		do {
			System.out.print(d+ " ");
			d--;
		} while (d>=0);
		System.out.println();
		System.out.println();
		// ВЫВОД НА КОНСОЛЬ ЧИСЕЛ 0 ДО 100 (положительные)

		int a = 0;

		do {
			System.out.print(a+ " ");
			a=a+2;
		} while (a<=100);
		System.out.println();
		System.out.println();
		// ВЫВОД НА КОНСОЛЬ ЧИСЕЛ 0 ДО 100 (через 3)
		int z = 0;

		do {
			System.out.print(z+ " ");
			z=z+3;
		} while (z<=100);
		System.out.println();
		System.out.println();
		// ВЫВОД НА КОНСОЛЬ ЧИСЕЛ 0 ДО 100 (через 5)
		int x = 0;

		do {
			System.out.print(x+ " ");
			x=x+5;
		} while (x<=100);
		System.out.println();
		System.out.println();

	}

}
