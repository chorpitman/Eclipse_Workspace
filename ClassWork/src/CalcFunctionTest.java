import static org.junit.Assert.*;

import org.junit.Test;


public class CalcFunctionTest {

	@Test
	public void testResultOfCalculationPLUS() 
	{
		int a=4;
		int b=2;
		char op='+';
		int res=0;
		res=CalcFunction.resultOfCalculation(a, b, op);
		assertEquals(6, res);	
	}
	
	@Test
	public void testResultOfCalculationMinus() 
	{
		int a=4;
		int b=2;
		char op='-';
		int res=0;
		res=CalcFunction.resultOfCalculation(a, b, op);
		assertEquals(2, res);	
	}

	@Test
	public void testResultOfCalculationSplit() 
	{
		int a=4;
		int b=2;
		char op='/';
		int res=0;
		res=CalcFunction.resultOfCalculation(a, b, op);
		assertEquals(2, res);	
	}
	
	@Test
	public void testResultOfCalculationMultiply() 
	{
		int a=4;
		int b=2;
		char op='*';
		int res=0;
		res=CalcFunction.resultOfCalculation(a, b, op);
		assertEquals(8, res);	
	}
	
}
