
public class Numbers {

	public static void main(String[] args) {
// ВЫВОД НА КОНСОЛЬ ЧИСЕЛ ОТ 20 ДО 50
		for (int i=20; i<=50; i++)
			System.out.print(i + "; ");
		System.out.println();
		System.out.println();

// ВЫВОД НА КОНСОЛЬ ЧИСЕЛ ОТ -10 ДО 20
		for (int i=-10; i<=20; i++)
			System.out.print(i + "; ");
		System.out.println();
		System.out.println();
		
// ВЫВОД НА КОНСОЛЬ ЧИСЕЛ ОТ 30 ДО 0
		for (int i=30; i>=0; i--)
			System.out.print(i + "; ");
		System.out.println();
		System.out.println();
		
// ВЫВОД НА КОНСОЛЬ ЧИСЕЛ ОТ 0 ДО 100 (ЧЕТНЫЕ)
		for (int i=0; i<=100; i=i+2 )
			System.out.print(i + "; ");
		System.out.println();
		System.out.println();
		
// ВЫВОД НА КОНСОЛЬ ЧИСЕЛ ОТ 0 ДО 100 (ЧЕРЕЗ 3)		
		for (int i=0; i<=100; i=i+3 )
			System.out.print(i + "; ");
		System.out.println();
		System.out.println();
		
// ВЫВОД НА КОНСОЛЬ ЧИСЕЛ ОТ 0 ДО 100 (ЧЕРЕЗ 5)
		for (int i=0; i<=100; i=i+5 )
			System.out.print(i + "; ");
		System.out.println();
		System.out.println();
		
	}

}
